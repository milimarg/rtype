#include "InputSystem.hpp"
#include "IndexedZipper.hpp"
#include "Using.hpp"
#define NOGDI

USING(ecs::sys::InputSystem);

void InputSystem::operator()(
    Registry &registry,
    Storage<Input> &inputs
) const
{
    for (auto &&[i, input] : IndexedZipper(inputs)) {
        input.checkInputs(registry, i);
        input.checkActions(registry, i);
    }
}
