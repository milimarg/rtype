#include "MouseSystem.hpp"
#include "Zipper.hpp"
#include "RaylibWrapper.hpp"

USING(
    ecs::sys::MouseSystem
);

void MouseSystem::operator()(
    [[maybe_unused]] const Registry &registry,
    Storage<Mouse> &mouses,
    Storage<Transformable> &transformables
) const
{
    Vec2 mousePos = G.getMousePosition();

    for (auto &&[mouse, transformable] : Zipper(mouses, transformables)) {
        auto &[x, y, _, __, ___, ____] = transformable;

        x = mousePos.x;
        y = mousePos.y;
    }
}
