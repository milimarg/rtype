#include "AssetManager.hpp"
#include "Using.hpp"

USING(ecs::AssetManager);

AssetManager &AssetManager::getInstance()
{
    static thread_local AssetManager assetManager;
    return assetManager;
}

bool AssetManager::removeAsset(const std::string &filename)
{
    if (!_assetCache.contains(filename))
        return false;
    _assetCache.erase(filename);
    return true;
}
